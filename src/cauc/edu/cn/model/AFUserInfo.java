package cauc.edu.cn.model;


/*
 * 用户类
 */

public class AFUserInfo implements BaseInfo{
	
	private Integer userid;
	
	private String usernum;
	private Integer age;
	private String sex;
	private String phonenum;
	private byte[] enpassword;
	private String username;	
	private String nonenpassword;
	

	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	public String getUsernum() {
		return usernum;
	}
	public void setUsernum(String usernum) {
		this.usernum = usernum;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public byte[] getEnpassword() {
		return enpassword;
	}
	public void setEnpassword(byte[] enpassword) {
		this.enpassword = enpassword;
	}
	public String getNonenpassword() {
		return nonenpassword;
	}
	public void setNonenpassword(String nonenpassword) {
		this.nonenpassword = nonenpassword;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getPhonenum() {
		return phonenum;
	}
	public void setPhonenum(String phonenum) {
		this.phonenum = phonenum;
	}
	public Integer getUserid() {
		return userid;
	}
	public void setUserid(Integer userid) {
		this.userid = userid;
	}
}
